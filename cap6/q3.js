export function codigoComTresFuncoes() {
  let resposta = `
Exemplo usando Number(), Math.round() e console.log():

const n1 = Number(prompt("Digite o primeiro número:"));
const n2 = Number(prompt("Digite o segundo número:"));
const n3 = Number(prompt("Digite o terceiro número:"));

const media = Math.round((n1 + n2 + n3) / 3);

console.log("Média arredondada:", media);
`;
  return resposta;
}
